# ─────────────────────────────────────────────

def plot_acf(ds, nlags=72):
    """Fig 6: ACF of effluent COD and TSS for 4 autocorrelation levels."""
    r_list = [0, 0.3, 0.6, 0.9]
    fig, axes = plt.subplots(1, 2, figsize=(13, 5))
    fig.suptitle("Autocorrelation Function (ACF) of Effluent — Effect of r (Typical CV)",
                 fontsize=13, fontweight='bold')

    lags = np.arange(nlags + 1)
    # 95% confidence interval for ACF (approx ±1.96/sqrt(n))
    conf_shown = False

    for ax, (key, title) in zip(axes, [
        ('eff_cod', 'Effluent COD'),
        ('eff_tss', 'Effluent TSS'),
    ]):
        for r in r_list:
            d = ds.get(('typical', r))
            if d is None:
                continue
            d = trim_stable(d)
            v = d.get(key, np.array([]))
            v = clean_outliers(v[~np.isnan(v)], key)
            if len(v) < nlags + 2:
                continue
            a = acf(v, nlags=nlags)
            n = len(v)
            ci = 1.96 / np.sqrt(n)
            ax.plot(lags, a, color=COLORS[r], linewidth=1.5, label=f"r = {r}")
            if not conf_shown:
                ax.axhline(ci,  color='gray', linestyle='--', lw=0.8, label='95% CI')
                ax.axhline(-ci, color='gray', linestyle='--', lw=0.8)
                conf_shown = True

        ax.axhline(0, color='black', lw=0.8)
        ax.set_xlim(0, nlags)
        ax.set_ylim(-0.3, 1.05)
        ax.set_xlabel("Lag (hours)")
        ax.set_ylabel("ACF")
        ax.set_title(title)
        ax.legend(fontsize=8)

    fig.tight_layout()
    return savefig("fig6_acf_r.png", fig)



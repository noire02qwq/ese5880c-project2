def plot_influent_histogram(ds):
    """Fig 2: Histogram of influent COD/TSS for 3 CV levels."""
    fig, axes = plt.subplots(2, 3, figsize=(13, 7))
    fig.suptitle("Influent Distribution — Effect of CV (r = 0.6)", fontsize=13, fontweight='bold')
    cv_list = ['small', 'typical', 'large']
    cv_labels = ['Small CV', 'Typical CV', 'Large CV']

    for col_idx, (cv, cv_lab) in enumerate(zip(cv_list, cv_labels)):
        d = ds.get((cv, 0.6))
        if d is None:
            for row in range(2):
                axes[row, col_idx].set_title(f"{cv_lab}\n(no data)", color='gray')
            continue
        d = trim_stable(d)

        for row_idx, (key, ylabel) in enumerate([
            ('inf_cod', 'Influent COD (mg/L)'),
            ('bio_tss', 'Bioreactor TSS (mg/L)'),
        ]):
            ax = axes[row_idx, col_idx]
            v = d.get(key, np.array([]))
            v = v[~np.isnan(v)]
            if len(v) == 0:
                continue
            ax.hist(v, bins=60, color=COLOR_CV[cv], alpha=0.75, edgecolor='none', density=True)
            mu, sd = np.mean(v), np.std(v)
            # Fit normal PDF
            xs = np.linspace(v.min(), v.max(), 300)
            ax.plot(xs, stats.norm.pdf(xs, mu, sd), 'k-', lw=1.5, label='Normal fit')
            ax.axvline(mu, color='navy', lw=1, linestyle='--', label=f'μ={mu:.1f}')
            ax.set_xlabel(ylabel, fontsize=9)
            ax.set_ylabel("Density" if col_idx == 0 else "")
            ax.set_title(f"{cv_lab}\n(CV row: {['COD','TSS'][row_idx]})")
            ax.legend(fontsize=7)

    fig.tight_layout()
    return savefig("fig2_influent_histogram_cv.png", fig)



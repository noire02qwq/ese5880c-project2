def plot_exceedance_cdf(ds):
    """Fig 5: CDF and exceedance probability for COD, TSS, Sludge TSS."""
    r_list = [0, 0.3, 0.6, 0.9]
    params = [
        ('eff_cod', 'Effluent COD (mg/L)', COD_STANDARD),
        ('eff_tss', 'Effluent TSS (mg/L)', None),  # TSS不显示标准线
        ('slu_tss', 'Sludge TSS (mg/L)',   None),
    ]

    fig, axes = plt.subplots(1, 3, figsize=(15, 5))
    fig.suptitle("Exceedance Probability (CCDF) — Effect of Autocorrelation (Typical CV)",
                 fontsize=13, fontweight='bold')

    for ax, (key, xlabel, std) in zip(axes, params):
        for r in r_list:
            d = ds.get(('typical', r))
            if d is None:
                continue
            d = trim_stable(d)
            v = d.get(key, np.array([]))
            v = clean_outliers(v[~np.isnan(v)], key)
            if len(v) == 0:
                continue
            v_sorted = np.sort(v)
            ccdf = 1 - np.arange(1, len(v)+1) / len(v)
            ax.semilogy(v_sorted, ccdf * 100, color=COLORS[r], alpha=0.85,
                        linewidth=1.5, label=f"r = {r}")

        if std is not None:
            ax.axvline(std, color='black', lw=1.5, linestyle='--', label=f'Standard\n({std} mg/L)')

        ax.set_xlabel(xlabel)
        ax.set_ylabel("Exceedance Probability (%)")
        ax.set_title(xlabel.split('(')[0].strip())
        ax.legend(fontsize=8)
        ax.set_ylim(bottom=0.01)

    fig.tight_layout()
    return savefig("fig5_exceedance_cdf_r.png", fig)



def load_csv(csv_path):
    """Load a BioWin CSV (pre-converted from xlsx) into a dict of numpy arrays."""
    import csv as csvmod
    print(f"  [load] {os.path.basename(csv_path)}")
    t0 = time.time()
    with open(csv_path, 'r', encoding='utf-8') as f:
        reader = csvmod.reader(f)
        header_row = next(reader)
        headers = [h.strip().strip('"') for h in header_row]
        # Map headers to internal keys
        col_keys = [NAME_MAP.get(h, None) for h in headers]
        # Collect data per column
        col_data = [[] for _ in headers]
        for row in reader:
            for i, val in enumerate(row):
                if i < len(col_data):
                    try:
                        col_data[i].append(float(val))
                    except (ValueError, TypeError):
                        col_data[i].append(np.nan)
    result = {}
    for key, vals in zip(col_keys, col_data):
        if key is not None:
            result[key] = np.array(vals, dtype=float)
    print(f"  [done] {os.path.basename(csv_path)} in {time.time()-t0:.1f}s  "
          f"(n={len(next(iter(result.values()), []))})")
    return result



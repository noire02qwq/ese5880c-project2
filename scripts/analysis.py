def plot_violation_summary(ds):
    """Fig 8: Bar chart of exceedance probability for COD/TSS at each r level."""
    r_list = [0, 0.3, 0.6, 0.9]
    params = [
        ('eff_cod', 'COD', COD_STANDARD),
        ('eff_tss', 'TSS', TSS_STANDARD),
    ]

    fig, axes = plt.subplots(1, 2, figsize=(10, 5))
    fig.suptitle("Discharge Standard Violation Rate (Exceedance Probability)\n— Typical CV",
                 fontsize=12, fontweight='bold')

    stats_data = {}

    for ax, (key, label, std) in zip(axes, params):
        probs = []
        for r in r_list:
            d = ds.get(('typical', r))
            if d is None:
                probs.append(0)
                continue
            d = trim_stable(d)
            v = d.get(key, np.array([]))
            v = clean_outliers(v[~np.isnan(v)], key)
            prob = exceedance_prob(v, std) if len(v) > 0 else 0
            probs.append(prob)
            stats_data.setdefault(label, {})[r] = prob

        bars = ax.bar([f"r={r}" for r in r_list], probs,
                      color=[COLORS[r] for r in r_list], alpha=0.8, edgecolor='white')
        for bar, prob in zip(bars, probs):
            ax.text(bar.get_x() + bar.get_width()/2, bar.get_height() + 0.1,
                    f'{prob:.2f}%', ha='center', va='bottom', fontsize=9, fontweight='bold')

        ax.set_ylabel("Exceedance Probability (%)")
        ax.set_title(f"Effluent {label} > {std:.0f} mg/L")
        ax.set_ylim(0, max(probs) * 1.3 + 0.5)

    fig.tight_layout()
    return savefig("fig8_violation_rate_r.png", fig), stats_data


# ─────────────────────────────────────────────
# STATISTICS SUMMARY
# ─────────────────────────────────────────────

def compute_stats(ds):
    """Compute summary statistics for all groups."""
    r_list = [0, 0.3, 0.6, 0.9]
    keys = [
        ('eff_cod', 'Effluent COD', COD_STANDARD),
        ('eff_tss', 'Effluent TSS', TSS_STANDARD),
        ('slu_tss', 'Sludge TSS',   None),
        ('bio_tss', 'Bioreactor TSS', None),
    ]
    rows = []
    for r in r_list:
        d = ds.get(('typical', r))
        if d is None:
            continue
        d = trim_stable(d)
        row = {'r': r}
        for key, name, std in keys:
            v = d.get(key, np.array([]))
            v = clean_outliers(v[~np.isnan(v)], key)
            if len(v) == 0:
                row[name + '_mean'] = np.nan
                row[name + '_std']  = np.nan
                row[name + '_cv']   = np.nan
                if std:
                    row[name + '_exc%'] = np.nan
                continue
            mu = np.mean(v)
            sd = np.std(v)
            row[name + '_mean'] = round(mu, 2)
            row[name + '_std']  = round(sd, 2)
            row[name + '_cv']   = round(sd/mu*100, 1) if mu > 0 else np.nan
            if std is not None:
                row[name + '_exc%'] = round(exceedance_prob(v, std), 3)
        rows.append(row)
    return rows



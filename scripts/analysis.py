def _parse_xlsx_to_arrays(xlsx_path, sheet_index=0):
    """Parse xlsx using streaming iterparse — fast even for 20MB XML files."""
    import xml.etree.ElementTree as ET

    NS = 'http://schemas.openxmlformats.org/spreadsheetml/2006/main'

    with zipfile.ZipFile(xlsx_path, 'r') as z:
        names = z.namelist()

        # ── Shared strings (small file, load fully) ──────────────────────
        shared = []
        if 'xl/sharedStrings.xml' in names:
            ss_data = z.read('xl/sharedStrings.xml')
            ss_root = ET.fromstring(ss_data)
            for si in ss_root.findall(f'{{{NS}}}si'):
                t = si.find(f'{{{NS}}}t')
                if t is not None and t.text:
                    shared.append(t.text)
                else:
                    # Concatenate all <t> children
                    texts = [t2.text or '' for t2 in si.findall(f'.//{{{NS}}}t')]
                    shared.append(''.join(texts))

        # ── Pick correct worksheet ─────────────────────────────────────
        sheet_files = sorted(
            [n for n in names if re.match(r'xl/worksheets/sheet\d+\.xml', n)],
            key=lambda x: int(re.search(r'sheet(\d+)', x).group(1))
        )
        if sheet_index >= len(sheet_files):
            sheet_index = 0
        target = sheet_files[sheet_index]

        # ── Stream-parse the (large) worksheet ───────────────────────────
        header_map = {}   # col_letter -> header name
        col_arrays  = {}  # col_letter -> list of floats
        current_row = None
        current_col = None
        current_type = None
        current_val  = None

        with z.open(target) as fh:
            for event, elem in ET.iterparse(fh, events=('start', 'end')):
                tag = elem.tag.split('}', 1)[-1] if '}' in elem.tag else elem.tag

                if event == 'start' and tag == 'row':
                    current_row = int(elem.get('r', '0'))

                elif event == 'start' and tag == 'c':
                    ref = elem.get('r', '')
                    # Extract column letter
                    current_col = ''.join(ch for ch in ref if ch.isalpha())
                    current_type = elem.get('t', '')
                    current_val  = None

                elif event == 'end' and tag == 'v':
                    current_val = elem.text

                elif event == 'end' and tag == 'c':
                    if current_val is None:
                        current_col = None
                        current_type = None
                        continue
                    if current_row == 1:
                        if current_type == 's':
                            idx = int(current_val)
                            header_map[current_col] = shared[idx] if idx < len(shared) else current_val
                    else:
                        if current_type != 's':  # skip string data cells
                            try:
                                fval = float(current_val)
                                col_arrays.setdefault(current_col, []).append(fval)
                            except (ValueError, TypeError):
                                pass
                    current_col = None
                    current_type = None
                    current_val  = None

                # Free memory — critical for large files
                if event == 'end' and tag == 'row':
                    elem.clear()

    if not header_map:
        raise ValueError(f"No header row found in {xlsx_path} sheet{sheet_index}")

    # Convert lists → numpy arrays; pad missing values with NaN
    max_len = max((len(v) for v in col_arrays.values()), default=0)
    result = {'_headers': header_map}
    for cl, vals in col_arrays.items():
        arr = np.full(max_len, np.nan)
        arr[:len(vals)] = vals
        result[cl] = arr

    return result


def _cache_path(xlsx_path, sheet_index):
    base = os.path.basename(xlsx_path).replace('.xlsx','')
    return os.path.join(CACHE_DIR, f"{base}_sheet{sheet_index}.npz")


def load_biowin(xlsx_path, sheet_index=0, force_reload=False):
    """Load BioWin xlsx output. Caches as npz for fast repeated access."""
    cache = _cache_path(xlsx_path, sheet_index)
    if os.path.exists(cache) and not force_reload:
        print(f"  [cache] {os.path.basename(xlsx_path)} sheet{sheet_index}")
        npz = np.load(cache, allow_pickle=True)
        return {k: npz[k] for k in npz.files}

    print(f"  [parse] {os.path.basename(xlsx_path)} sheet{sheet_index} ...")
    t0 = time.time()
    raw = _parse_xlsx_to_arrays(xlsx_path, sheet_index)
    headers = raw['_headers']

    # Build standardized dict by looking up known column names
    # Map header string → key
    NAME_MAP = {
        'Time (days)':                             'time',
        'Time (days)_(365days)':                   'time',
        'Influent - COD0 BOD - Total Carbonaceous':'inf_bod',
        'Influent - COD0 COD - Total':             'inf_cod',
        'Bioreactor1 Total suspended solids':      'bio_tss',
        'Bioreactor1 Volatile suspended solids':   'bio_vss',
        'Bioreactor1 OUR - Carbonaceous':          'bio_our_c',
        'Bioreactor1 Element HRT':                 'bio_hrt',
        'Bioreactor1 OUR - Total':                 'bio_our_t',
        'Effluent3 BOD - Total Carbonaceous':      'eff_bod',
        'Effluent3 Total suspended solids':        'eff_tss',
        'Effluent3 COD - Filtered':                'eff_cod',
        'Effluent3 Flow':                          'eff_flow',
        'Sludge8 BOD - Total Carbonaceous':        'slu_bod',
        'Sludge8 COD - Total':                     'slu_cod',
        'Sludge8 Total suspended solids':          'slu_tss',
        'Sludge8 Volatile suspended solids':       'slu_vss',
        'Sludge8 Flow':                            'slu_flow',
    }

    data = {}
    for col_l, hdr in headers.items():
        key = NAME_MAP.get(hdr)
        if key and col_l in raw:
            data[key] = raw[col_l]

    if 'time' not in data:
        # Use first numeric array as time
        for col_l in sorted(raw.keys()):
            if col_l != '_headers':
                data.setdefault('time', raw[col_l])
                break

    np.savez_compressed(cache, **data)
    print(f"  [done]  {os.path.basename(xlsx_path)} sheet{sheet_index} in {time.time()-t0:.1f}s")
    return data



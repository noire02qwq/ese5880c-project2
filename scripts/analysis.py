def clean_outliers(v, key):
    """Remove obvious outliers based on data type."""
    if len(v) == 0:
        return v
    if key in ['slu_tss', 'slu_bod']:
        # 剩余污泥: 删除 <1000 和 >3000
        mask = (v >= 1000) & (v <= 3000)
        return v[mask]
    elif key in ['eff_cod', 'inf_cod']:
        # COD: 删除 <10 的异常值
        return v[v >= 10]
    elif key in ['eff_tss', 'bio_tss']:
        # TSS: 删除 <1 的异常值
        return v[v >= 1]
    elif key in ['eff_flow', 'slu_flow']:
        # 流量: 保持正值
        return v[v > 0]
    return v


def trim_stable(data, warmup_days=180):
    """Remove first `warmup_days` of data (warm-up period)."""
    out = {}
    t = data.get('time', np.array([]))
    if len(t) == 0:
        return data
    mask = t >= warmup_days
    for k, v in data.items():
        if isinstance(v, np.ndarray) and len(v) == len(t):
            out[k] = v[mask]
        else:
            out[k] = v
    return out


def exceedance_prob(values, threshold):
    """P(X > threshold)"""
    v = values[~np.isnan(values)]
    return np.mean(v > threshold) * 100  # percent


def acf(x, nlags=50):
    """Manual autocorrelation function."""
    x = x[~np.isnan(x)]
    n = len(x)
    x_centered = x - x.mean()
    var = np.dot(x_centered, x_centered)
    acf_vals = []
    for lag in range(nlags + 1):
        if lag == 0:
            acf_vals.append(1.0)
        else:
            cov = np.dot(x_centered[:n-lag], x_centered[lag:])
            acf_vals.append(cov / var)
    return np.array(acf_vals)


